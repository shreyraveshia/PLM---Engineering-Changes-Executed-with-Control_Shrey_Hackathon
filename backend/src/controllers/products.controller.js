const update = async (req, res) => {
  const { id } = req.params;
  const { name, sale_price, cost_price, attachment, notes } = req.body;
  try {
    const existing = await query('SELECT * FROM products WHERE id = $1', [id]);
    if (existing.rows.length === 0) {
      return res.status(404).json({ error: 'Product not found' });
    }
    const product = existing.rows[0];
    if (product.status === 'archived') {
      return res.status(400).json({ error: 'Cannot edit an archived product. Create an ECO to propose changes.' });
    }

    /* What does "archived" mean?
    Archived means the product is retired/inactive — it's no longer in use, but it's not deleted either.
    
    When does archiving happen?
    Two situations:
    Situation 1 — Manual archive:
    Admin clicks the Archive button on a product directly. Maybe the product is discontinued — the company stopped making Wooden Tables forever.
    
    Situation 2 — Automatic archive via ECO:
    When an ECO is approved with version_update = true:
    Wooden Table v1 → gets ARCHIVED automatically
    Wooden Table v2 → becomes ACTIVE (new version)
    The old version gets archived to preserve history. The new version takes over.

    */

    /* How COALESCE($1, name) Works:
    COALESCE takes a list of values and returns the first one that isn't NULL.
    Scenario A (Update Name): If you pass a new name in $1, the database sees COALESCE('New Name', name)
     and picks 'New Name'.
    Scenario B (Keep Name): If you don't send a name (it arrives as null via $1), the database sees 
    COALESCE(null, name). It skips the null and picks the existing name already stored in that row.
    */
    const result = await query(
      `UPDATE products
       SET name = COALESCE($1, name),
           sale_price = COALESCE($2, sale_price),
           cost_price = COALESCE($3, cost_price),
           attachment = COALESCE($4, attachment),
           notes = COALESCE($5, notes),
           updated_at = NOW() 
       WHERE id = $6
       RETURNING *`,
      [name || null, sale_price || null, cost_price || null, attachment || null, notes || null, id]
    );

    /* This pattern:-
    It prevents you from having to write different SQL queries for every possible combination of updates.
    Whether the user changes just the notes or the entire product, this one query handles it all safely.
    */

    /* updated_at = NOW(): This manually forces the "last updated" timestamp to the current time, 
       even if no other data actually changed.
  
     WHERE id = $6:(with id as in params) This ensures you are only updating the one specific product you intended to change.
  RETURNING *: Just like with the INSERT, this sends the final, updated row back to your JavaScript code 
  so you can see the new values and the fresh updated_at time.
  */



    await logAudit(ACTIONS.PRODUCT_UPDATED, 'product', id, product, result.rows[0], req.user.id);
    return res.json(result.rows[0]);
  } catch (err) {
    return res.status(500).json({ error: err.message });
  }
};
// product is the old data (fetched before update). 
// result.rows[0] is the new data (returned after update). 





const archive = async (req, res) => {
  const { id } = req.params;
  try {
    const existing = await query('SELECT * FROM products WHERE id = $1', [id]);
    if (existing.rows.length === 0) {
      return res.status(404).json({ error: 'Product not found' });
    }
    if (existing.rows[0].status === 'archived') {
      return res.status(400).json({ error: 'Product is already archived' });
    }



    /* Before archiving, check if any open ECOs exist for this product. 
    You cannot archive a product that has active change requests in progress — 
    that would leave those ECOs in a broken state pointing to an archived product.
    */
    const activeEcos = await query(
      `SELECT id FROM ecos WHERE product_id = $1 AND status = 'open'`,
      [id]
    );
    /* 
    This query checks if there are any rows in the ecos table where:

product_id matches the product we're trying to archive.
status is 'open' (meaning it's an active change request, not approved or cancelled).
If activeEcos.rows.length is greater than 0, it means there's at least one open ECO linked to this product.
    */
    if (activeEcos.rows.length > 0) {
      return res.status(400).json({
        error: `Cannot archive product. It has ${activeEcos.rows.length} open ECO(s). Close or cancel them first.`
      });
    }

    //If no open ECOs — proceed with archiving:
    const result = await query(
      `UPDATE products SET status = 'archived', updated_at = NOW() WHERE id = $1 RETURNING *`,
      [id]
    );

    await logAudit(ACTIONS.PRODUCT_ARCHIVED, 'product', id,
      { status: 'active' }, { status: 'archived' }, req.user.id);
    return res.json(result.rows[0]);
  } catch (err) {
    return res.status(500).json({ error: err.message });
  }
};



/* What this function does in one line:
"Give me ALL versions of a product — from v1 to the latest — no matter which version I ask from."

The Problem it Solves
Imagine this family tree:
Wooden Table v1 (id=1, parent_id=NULL)  ← original
      ↓
Wooden Table v2 (id=5, parent_id=1)     ← after ECO-001
      ↓
Wooden Table v3 (id=9, parent_id=5)     ← after ECO-003

If judge clicks on v2 (id=5) and asks for history — we need to show v1, v2, v3 all together. But we only have id=5. How do we find v1 and v3?
That's exactly what this function solves.


**WITH RECURSIVE** = a loop in SQL. :-

""""product_tree""""" is the name you gave to the entire recursive query itself at the top:
So product_tree is not a real table in your database. It's a temporary result set that you named yourself —


**How it works step by step:**
```
Round 1: Start with root id=1
         product_tree = {1}

Round 2: Find all products WHERE parent_id IN {1}
         Finds v2 (id=5, parent_id=1)
         product_tree = {1, 5}

Round 3: Find all products WHERE parent_id IN {1, 5}
         Finds v3 (id=9, parent_id=5)
         product_tree = {1, 5, 9}

Round 4: Find all products WHERE parent_id IN {1, 5, 9}
         Finds nothing → STOP
         Final result = {1, 5, 9}

It keeps going DOWN the family tree until no more children exist.

*/



/*
THE PROBLEM TO SOLVE
The user might click "View History" from any version — v1, v2, or v3.
Regardless of which version they clicked, you need to find ALL versions. 

So first you need to find the root — the very first version (v1).
*/

const getHistory = async (req, res) => {
  const { id } = req.params;
  try {

    /* 
    Before showing the history, the code needs to find the "original" product.
    If the product you clicked on is already the original, it uses its own id.
    If it’s a newer version (a child), COALESCE grabs the parent_id.
    This ensures that no matter which version you are looking at, you get the entire family tree.
    */



    const rootResult = await query(
      'SELECT COALESCE(parent_id, id) as root_id FROM products WHERE id = $1',
      [id]
    );


    /* The Logic:
    If you are looking at Version 3 (which has a parent_id), 
    COALESCE grabs that parent_id (Version 2). 
    If you are looking at Version 1 (which has no parent), it just uses its own id.

    The Goal: It tries to jump back at least one step toward the "ancestor" 
    so it can start searching downwards from there.

*/
    if (rootResult.rows.length === 0) {
      return res.status(404).json({ error: 'Product not found' });
    }

    const rootId = rootResult.rows[0].root_id;

    // This is the most complex part. A Recursive CTE (WITH RECURSIVE) is a query that loops over
    // itself until it finds every related row.

    /* 1. The Anchor (The Start)
    sql
    SELECT id FROM products WHERE id = $1  -- $1 is the rootId we just found
    Use code with caution.
    
    It puts the rootId into a temporary list called product_tree.
    
    
    
    2. The Recursion (The Loop)
    sql
    SELECT p.id FROM products p
    INNER JOIN product_tree pt ON p.parent_id = pt.id
    
    */


    const result = await query(
      `WITH RECURSIVE product_tree AS (
         SELECT id FROM products WHERE id = $1
         UNION ALL
         SELECT p.id FROM products p
         INNER JOIN product_tree pt ON p.parent_id = pt.id
       )
       SELECT p.*, u.name as created_by_name
       FROM products p
       LEFT JOIN users u ON p.created_by = u.id
       WHERE p.id IN (SELECT id FROM product_tree)
          OR p.parent_id IN (SELECT id FROM product_tree)
          OR p.id = $1
       ORDER BY p.version ASC`,
      [rootId]
    );
    // ORDER BY p.version ASC
    // Returns: v1 first → v2 → v3 last

    /*
    `p.id IN (SELECT id FROM product_tree)` — fetch all products whose id is in our collected list [1, 5, 9].
    Condition 1: p.id IN product_tree
                 → include all found versions directly
     
     Condition 2: p.parent_id IN product_tree
                 → include any siblings or cousins
     Condition 3: p.id = $1
                 → always include the originally requested product
                 → safety net in case recursive missed it
    
    
    
    
     */


    return res.json(result.rows);
  } catch (err) {
    return res.status(500).json({ error: err.message });
  }
};


