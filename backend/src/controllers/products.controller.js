const { query } = require('../config/db');
const { logAudit, ACTIONS } = require('../utils/auditLogger');

const getAll = async (req, res) => {
  try {
    const { status, search } = req.query;
    /* `req.query` contains URL query parameters. If frontend calls:
GET /api/products?status=active&search=table
Then status = 'active' and search = 'table'. */

    // 

    let sql = `
      SELECT p.id, p.name, p.sale_price, p.cost_price, p.version,
             p.status, p.parent_id, p.attachment, p.notes,
             p.created_at, p.updated_at,
             u.name AS created_by_name
      FROM products p
      LEFT JOIN users u ON p.created_by = u.id
      WHERE 1=1
    `; /* WHERE 1=1 — always true condition. 
    This is a trick to make dynamic query building easier.
    Since you'll be adding optional AND conditions below, 
    starting with WHERE 1=1 means you can always safely append 'AND something'
    without worrying about whether it's the first condition or not. */
    const params = [];
    let paramIdx = 1;

    /* Think of it like a numbered seat at a stadium:
    The SQL string uses paramIdx to create "reserved seats" (like $1, $2, $3).
    The params array holds the "people" who will sit in those seats, in the exact order they were added. */



    if (status) {
      sql += ` AND p.status = $${paramIdx}`;
      params.push(status);
      paramIdx++;
    }
    if (search) {
      sql += ` AND p.name ILIKE $${paramIdx}`;
      params.push(`%${search}%`);
      paramIdx++;
    } // %${search}% — the % is a wildcard in SQL. 
    // %table% means "anything before and after the word table".
    // So searching "table" finds "Wooden Table", "Coffee Table", "Table Lamp".

    /*
    This is dynamic query building. 
    You build the SQL string piece by piece depending on what filters were passed.
    */

    /* ILIKE — case-insensitive LIKE search. Regular LIKE is case-sensitive. 
    ILIKE 'table' matches "Table", "TABLE", "table" all the same */

    sql += ' ORDER BY p.created_at DESC';
    /* Adds ordering at the end — newest products first. */

    const result = await query(sql, params);
    return res.json(result.rows);
  } catch (err) {
    console.error('getAll products error:', err);
    return res.status(500).json({ error: err.message });
  }
};
// Then runs the completed dynamic query and returns all rows.

/* When you call query(sql, params), you are handing the database two separate things:
1)The Map (SQL): ... WHERE 1=1 AND p.status = $1 AND p.name ILIKE $2
2)The Data (params): ['active', '%table%']
The database sees $1 and looks at the 1st item in the array. It sees $2 and looks at the 2nd item.
 */





const getById = async (req, res) => {
  const { id } = req.params;
  // req.params contains URL path parameters.
  // If the route is /api/products/:id and someone calls /api/products/5, then id = '5'.

  try {
    const result = await query( // query(sql, params);
      `SELECT p.id, p.name, p.sale_price, p.cost_price, p.version,
              p.status, p.parent_id, p.attachment, p.notes,
              p.created_at, p.updated_at,
              u.name AS created_by_name, u.id AS created_by_id
       FROM products p
       LEFT JOIN users u ON p.created_by = u.id
       WHERE p.id = $1`,
      [id]
    );
    if (result.rows.length === 0) {
      return res.status(404).json({ error: `Product with id ${id} not found` });
    }
    return res.json(result.rows[0]);
  } catch (err) {
    return res.status(500).json({ error: err.message });
  }
};



const create = async (req, res) => {
  const { name, sale_price, cost_price, attachment, notes } = req.body;

  if (!name || name.trim() === '') {
    return res.status(400).json({ error: 'Product name is required' });
  }
  // Only name is required. Everything else is optional.
  // req.body contains the data sent from the frontend.

  // If the frontend sends:
  // { "name": "Table", "sale_price": 100 }
  // Then name = "Table" and sale_price = 100.

  try {
    const result = await query(
      `INSERT INTO products (name, sale_price, cost_price, version, status, attachment, notes, created_by)
       VALUES ($1, $2, $3, 1, 'active', $4, $5, $6)
       RETURNING *`,
      [name.trim(), sale_price || null, cost_price || null, attachment || null, notes || null, req.user.id]
    );
    /* 
   Notice version is hardcoded to 1 and status hardcoded to 'active'.
   Every new product always starts at version 1, always starts as active. 
   These are never user-supplied.

RETURNING * — returns the complete inserted row including the auto-generated id and created_at.

Ex- {
id: 101,             // Generated by DB
name: "Screws",
sale_price: 5.00,
cost_price: 2.00,
version: 1,          // Hardcoded in SQL
status: "active",    // Hardcoded in SQL
created_at: "2023-10-27T...", // Generated by DB
// ... and so on
}
 
  sale_price || null — if sale_price wasn't sent, store NULL instead of undefined.
  PostgreSQL doesn't understand JavaScript's undefined.

  
  req.user.id — who created this product. 
  Remember req.user was set by verifyToken middleware.
   */


    const newProduct = result.rows[0];
    await logAudit(ACTIONS.PRODUCT_CREATED, 'product', newProduct.id, null, newProduct, req.user.id);
    return res.status(201).json(newProduct);
  } catch (err) {
    return res.status(500).json({ error: err.message });
  }
};

